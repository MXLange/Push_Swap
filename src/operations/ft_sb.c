#include "../../push_swap.h"

void	ft_sb(t_stack **b)
{
	t_stack	*temp;

	temp = (*b)->next;
	(*b)->next = temp->next;
	temp->next = (*b);
	(*b) = temp;
	ft_printf("sb\n");
}
